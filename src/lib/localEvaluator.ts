export async function evaluateTestLocal(answers: Record<number, string[]>): Promise<TestResult> {
  // 模拟网络延迟，让用户有“正在分析”的体验
  await new Promise(resolve => setTimeout(resolve, 2000));

  // 统计用户在5个维度上的倾向 (A, B, C, D)
  const dimensionScores: Record<number, Record<string, number>> = {
    1: { A: 0, B: 0, C: 0, D: 0 },
    2: { A: 0, B: 0, C: 0, D: 0 },
    3: { A: 0, B: 0, C: 0, D: 0 },
    4: { A: 0, B: 0, C: 0, D: 0 },
    5: { A: 0, B: 0, C: 0, D: 0 },
  };

  // 题目与维度的映射关系 (假设每3题对应一个维度)
  for (let i = 1; i <= 15; i++) {
    const dimension = Math.ceil(i / 3);
    const userChoices = answers[i] || [];
    userChoices.forEach(choice => {
      if (dimensionScores[dimension][choice] !== undefined) {
        dimensionScores[dimension][choice] += 1;
      }
    });
  }

  // 找出用户在每个维度上的主要倾向
  const userTraits: string[] = [];
  for (let d = 1; d <= 5; d++) {
    const scores = dimensionScores[d];
    let maxTrait = 'A';
    let maxScore = -1;
    for (const trait of ['A', 'B', 'C', 'D']) {
      if (scores[trait] > maxScore) {
        maxScore = scores[trait];
        maxTrait = trait;
      }
    }
    userTraits.push(maxTrait);
  }

  // 寻找最匹配的角色
  let bestMatch = charactersData[0];
  let maxMatchScore = -1;

  charactersData.forEach(char => {
    let matchScore = 0;
    for (let i = 0; i < 5; i++) {
      if (char.traits[i] === userTraits[i]) {
        matchScore += 2; // 完全匹配加2分
      } else if (
        // 相似性格加1分 (简单设定 A/C 偏冷/理性，B/D 偏热/感性)
        (char.traits[i] === 'A' && userTraits[i] === 'C') ||
        (char.traits[i] === 'C' && userTraits[i] === 'A') ||
        (char.traits[i] === 'B' && userTraits[i] === 'D') ||
        (char.traits[i] === 'D' && userTraits[i] === 'B')
      ) {
        matchScore += 1;
      }
    }
    // 引入一点随机性，防止相同选项总是匹配同一个人（如果得分相同）
    if (matchScore > maxMatchScore || (matchScore === maxMatchScore && Math.random() > 0.5)) {
      maxMatchScore = matchScore;
      bestMatch = char;
    }
  });

  // 拼接深度解析文本
  const analysisParagraphs = [
    `经过对您选择的深度解析，您的灵魂代码为 [${userTraits.join('-')}]。`,
    dimensionTexts[1][userTraits[0]],
    dimensionTexts[2][userTraits[1]],
    dimensionTexts[3][userTraits[2]],
    dimensionTexts[4][userTraits[3]],
    dimensionTexts[5][userTraits[4]],
    `综上所述，您与【${bestMatch.name}】的灵魂共鸣度最高。你们在面对世界的残酷时，都选择了一种独特而坚韧的姿态。无论前路如何，请保持这份属于你的底色。`
  ];

  return {
    characterName: bestMatch.name,
    biography: bestMatch.bio,
    analysis: analysisParagraphs.join('\n\n')
  };
}
